set1 = {1,2,3,4,5}
set2 = {4,5,6,7,8}

print("--------------Union--------------")
print(set1 | set2)


print("-------------difference--------------")
print(set1 - set2)
print(set1.difference(set2))

print("------------inter section--------------")
print(set1 & set2)
print(set1.intersection(set2))

print("--------------semmetric difference------------")
print(set1.symmetric_difference(set2))

